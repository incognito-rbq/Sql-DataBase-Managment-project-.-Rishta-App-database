DROP DATABASE IF EXISTS dbpp;
CREATE DATABASE dbpp;
USE dbpp;

CREATE TABLE Preference (
  preference_id INT NOT NULL,
  country VARCHAR(30) NOT NULL,
  education VARCHAR(100) NOT NULL,
  height INT NOT NULL,
  min_age INT NOT NULL CHECK (min_age > 18),
  max_age INT NOT NULL,
  religion VARCHAR(100) NOT NULL,
  PRIMARY KEY (preference_id)
);

CREATE TABLE Transaction (
  transaction_id INT NOT NULL,
  amount double ,
  payment_method VARCHAR(100),
  date DATE,
  PRIMARY KEY (transaction_id)
);

CREATE TABLE Subscription (
  details_id INT not null,
  subscription_id INT NOT NULL,
  subscription_plan VARCHAR(100),
  start_date DATE NOT NULL,
  expire_date DATE NOT NULL,
  PRIMARY KEY (subscription_id)
);

CREATE TABLE Subscription_details (
  details_id INT NOT NULL,
  transaction_id INT NOT NULL,
  subscription_id INT NOT NULL,
  PRIMARY KEY (details_id),
  FOREIGN KEY (transaction_id) REFERENCES Transaction(transaction_id) on update cascade,
  FOREIGN KEY (subscription_id) REFERENCES Subscription(subscription_id)  on update cascade
);

CREATE TABLE Notification (
  notification_id INT NOT NULL,
  details VARCHAR(300) NOT NULL,
  message VARCHAR(300) NOT NULL,
  date DATE NOT NULL,
  time TIME NOT NULL,
  PRIMARY KEY (notification_id)
);

CREATE TABLE User (
  user_id INT not null,
  first_name VARCHAR(50) NOT NULL,
  last_name VARCHAR(50) NOT NULL,
  gender VARCHAR(10) NOT NULL,
  age INT NOT NULL,
  email VARCHAR(50) NOT NULL,
  password VARCHAR(255) NOT NULL,
  contact_no VARCHAR(25) NOT NULL,
  subscription_id INT NOT NULL,
  profile_id INT NOT NULL,
  preference_id INT NOT NULL,
  notification_id INT NOT NULL,
  PRIMARY KEY (user_id),
  FOREIGN KEY (preference_id) REFERENCES Preference(preference_id)  on update cascade,
  FOREIGN KEY (subscription_id) REFERENCES Subscription(subscription_id)  on update cascade,
  FOREIGN KEY (notification_id) REFERENCES Notification(notification_id)  on update cascade
);
CREATE TABLE Admin (
  admin_id INT NOT NULL,
  first_name VARCHAR(50) NOT NULL,
  last_name VARCHAR(50) NOT NULL,
  email VARCHAR(50) NOT NULL,
  contact_no VARCHAR(25) NOT NULL,
  PRIMARY KEY (admin_id)
);

CREATE TABLE Suggestions (
  suggestion_id INT NOT NULL,
  compatibility_score INT NOT NULL,
  admin_id INT NOT NULL,
  PRIMARY KEY (suggestion_id),
  FOREIGN KEY (admin_id) REFERENCES Admin(admin_id)  on update cascade
);
CREATE TABLE Profile (
  profile_id INT NOT NULL,
  country VARCHAR(30) NOT NULL,
  city VARCHAR(30) NOT NULL,
  religion VARCHAR(100) NOT NULL,
  education VARCHAR(100) NOT NULL,
  height INT NOT NULL,
  weight INT NOT NULL,
  income INT NOT NULL,
  profession VARCHAR(80) NOT NULL,
  bio VARCHAR(300) NOT NULL,
  profile_picture VARCHAR(50) NOT NULL,
  marital_status BOOLEAN NOT NULL,
  suggestion_id INT NOT NULL,
  user_id INT NOT NULL,
  PRIMARY KEY (profile_id),
  FOREIGN KEY (user_id) REFERENCES User (user_id)  on update cascade,
  FOREIGN KEY (suggestion_id) REFERENCES Suggestions(suggestion_id)  on update cascade
);

CREATE TABLE Match_request (
  request_id INT NOT NULL,
  request_date DATE NOT NULL,
  time TIME NOT NULL,
  message VARCHAR(300) NOT NULL,
  sender_id INT NOT NULL,
  receiver_id INT NOT NULL,
  PRIMARY KEY (request_id),
  FOREIGN KEY (sender_id) REFERENCES Profile(profile_id)  on update cascade,
  FOREIGN KEY (receiver_id) REFERENCES User(user_id)  on update cascade
);

CREATE TABLE Completed_match (
  couple_id INT NOT NULL,
  match_date DATE NOT NULL,
  likes INT NOT NULL,
  status BOOLEAN NOT NULL,
  match_id INT NOT NULL,
  PRIMARY KEY (couple_id),
  FOREIGN KEY (match_id) REFERENCES Match_request(request_id)  on update cascade
);

CREATE TABLE Feedback (
  review_id INT NOT NULL,
  date DATE NOT NULL,
  comments VARCHAR(300),
  rating_star INT NOT NULL CHECK (rating_star > 0 AND rating_star < 6),
  couple_id INT NOT NULL,
  PRIMARY KEY (review_id),
  FOREIGN KEY (couple_id) REFERENCES Completed_match(couple_id)  on update cascade
);

CREATE TABLE Consultant (
  consultant_id INT NOT NULL,
  first_name VARCHAR(50) NOT NULL,
  last_name VARCHAR(50) NOT NULL,
  email VARCHAR(50) NOT NULL,
  contact_no VARCHAR(25) NOT NULL,
  PRIMARY KEY (consultant_id)
);

CREATE TABLE Support_FAQs (
  faq_id INT NOT NULL,
  query VARCHAR(500) NOT NULL,
  date DATE NOT NULL,
  status BOOLEAN NOT NULL,
  user_id INT NOT NULL,
  consultant_id INT NOT NULL,
  PRIMARY KEY (faq_id),
  FOREIGN KEY (user_id) REFERENCES User (user_id)  on update cascade,
  FOREIGN KEY (consultant_id) REFERENCES Consultant (consultant_id)  on update cascade
);
